## Header parsing helper:
def extract_offsets(header):
    prefixes = ['IMAGE', 'XDIFF', 'YDIFF']
    values = []
    for pref in prefixes:
        keys = [k for k in header.keys() if k.startswith(pref)]
        values.append(np.array([header[k] for k in keys]))
    inames, xdiffs, ydiffs = values
    return inames, -xdiffs, -ydiffs

