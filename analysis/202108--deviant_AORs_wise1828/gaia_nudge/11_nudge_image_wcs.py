##--------------------------------------------------------------------------##
## Iterate over chosen images:
for ipath in use_images:
    sys.stderr.write("%s\n" % fulldiv)
    sys.stderr.write("ipath: %s\n" % ipath)
    cpath = '%s.%s' % (ipath, cat_type)
    sys.stderr.write("cpath: %s\n" % cpath)
    if not os.path.isfile(cpath):
        sys.stderr.write("Yikes, file not found: %s\n" % cpath)
    ibase = os.path.basename(ipath)

    # load extended catalog:
    chdrs = pf.getheader(cpath, extname='CATALOG')
    ra_nudge = chdrs['GRADELTA']
    de_nudge = chdrs['GDEDELTA']

    # load original image:
    idata, ihdrs = pf.getdata(ipath, header=True)

    # duplicate headers and nudge CRVALs:
    nhdrs = ihdrs.copy()
    nhdrs['CRVAL1'] += ra_nudge
    nhdrs['CRVAL2'] += de_nudge

    # save duped and nudged files:
    dpath = '%s/%s' % (dupe_dir, ibase)
    npath = '%s/%s' % (save_dir, ibase)
    qsave(dpath, idata, header=ihdrs)
    qsave(npath, idata, header=nhdrs)
    #sys.stderr.write("ztf -r nearby.reg %s %s\n" % (dpath, npath))
    sys.stderr.write("flztfs %s %s\n" % (dpath, npath))

