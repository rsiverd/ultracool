## A Gaussian distribution for overplotting:
def gaussian(x, mu, sigma):
    zz = (x - mu) / sigma
    inv_norm = np.sqrt(2.0 * np.pi) * sigma
    return np.exp(-0.5 * zz * zz) / inv_norm


